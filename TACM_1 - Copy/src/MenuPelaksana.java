    private void loadAdmin() {
          try {
            // Membuat statement dari koneksi
            Statement stmt = conek1.GetConnection().createStatement();
            
            // Query untuk mendapatkan ID dan nama admin
            String sql = "SELECT id_admin, Nama FROM admin_pelaksana where id_perusahaan = '" + userIdP +"';" ;
            ResultSet rs = stmt.executeQuery(sql);
            
            // Tambahkan pilihan default ke dropdown
            cmbAdmin.addItem("- Pilih Admin -");
            
            // Tambahkan admin ke dropdown dan HashMap
            while (rs.next()) {
                String idAdmin = rs.getString("id_admin"); // ID bertipe String
                String namaAdmin = rs.getString("Nama");
                cmbAdmin.addItem(namaAdmin);         // Tambahkan nama admin ke dropdown
                adminMap.put(namaAdmin, idAdmin);
                
            }
             cmbAdmin.repaint();
            // Menutup statement
            stmt.close();
           
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Gagal terhubung ke database: " + e.getMessage());
        }
    }

