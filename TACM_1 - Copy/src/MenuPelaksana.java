        String query = "UPDATE proyek SET nama_proyek = '" + namaproyek + "', lokasi = '" + lokasiproyek +
                "', estimasi = '" + estimasi + "' WHERE ID = '" + id+ "';";
        
        // Gunakan Statement untuk menjalankan query
         Statement stmt =conek1.GetConnection().createStatement();
